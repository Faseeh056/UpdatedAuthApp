<script lang="ts">
  import Card from '$lib/components/ui/Card.svelte';
  import Button from '$lib/components/ui/Button.svelte';
  import Input from '$lib/components/ui/Input.svelte';
  import { goto } from '$app/navigation';
  import { invalidateAll } from '$app/navigation';
  import { signIn } from '@auth/sveltekit/client';
  import { onMount } from 'svelte';

  let { data } = $props();
  let { session } = data;

  // Get role from URL params
  let role = $state('client');
  let email = $state('');
  let password = $state('');
  let loading = $state(false);
  let oauthLoading = $state({ google: false, github: false });
  let error = $state('');
  let success = $state('');

  onMount(() => {
    if (typeof window !== 'undefined') {
      const urlParams = new URLSearchParams(window.location.search);
      role = urlParams.get('role') || 'client';
      
      // Check for OAuth errors
      const oauthError = urlParams.get('error');
      if (oauthError === 'oauth_failed') {
        error = 'OAuth authentication failed. Please try again.';
      }
    }
  });

  // Modern OAuth handlers with proper error handling
  async function handleGoogleSignIn() {
    try {
      oauthLoading.google = true;
      error = '';
      console.log('üîê Starting Google OAuth...');
      
      await signIn('google', { 
        callbackUrl: '/dashboard',
        redirect: true 
      });
      
    } catch (err) {
      console.error('‚ùå Google sign in error:', err);
      error = 'Failed to sign in with Google. Please try again.';
    } finally {
      oauthLoading.google = false;
    }
  }

  async function handleGitHubSignIn() {
    try {
      oauthLoading.github = true;
      error = '';
      console.log('üîê Starting GitHub OAuth...');
      
      await signIn('github', { 
        callbackUrl: '/dashboard',
        redirect: true 
      });
      
    } catch (err) {
      console.error('‚ùå GitHub sign in error:', err);
      error = 'Failed to sign in with GitHub. Please try again.';
    } finally {
      oauthLoading.github = false;
    }
  }

  async function handleSubmit(e: Event) {
    e.preventDefault();
    loading = true;
    error = '';
    success = '';

    try {
      await signIn('credentials', {
        email,
        password,
        role,
        redirect: true,
        callbackUrl: '/dashboard'
      });
    } catch (err) {
      error = 'Network error. Please try again.';
      loading = false;
    }
  }
</script>

<svelte:head>
  <title>Sign In | Auth App</title>
  <meta name="description" content="Sign in to your account with secure authentication" />
</svelte:head>

<div class="min-h-screen bg-gradient-to-br from-blue-50 via-white to-indigo-50 relative overflow-hidden">
