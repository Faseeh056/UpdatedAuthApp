  </div>

  <div class="relative z-10 min-h-screen flex items-center justify-center p-4 sm:p-8">
    <div class="max-w-md w-full mx-auto">
      <!-- Modern Login Card -->
      <div class="bg-white/80 backdrop-blur-xl rounded-3xl p-8 sm:p-12 shadow-2xl border border-white/20">
        <!-- Header Section -->
        <div class="text-center mb-10">
          <div class="flex justify-center mb-6">
            <div class="w-20 h-20 bg-gradient-to-br from-blue-600 to-indigo-600 rounded-2xl flex items-center justify-center shadow-lg">
              <svg class="w-10 h-10 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M12 15v2m-6 4h12a2 2 0 002-2v-6a2 2 0 00-2-2H6a2 2 0 00-2 2v6a2 2 0 002 2zm10-10V7a4 4 0 00-8 0v4h8z"></path>
              </svg>
            </div>
          </div>
          <h1 class="text-3xl sm:text-4xl font-bold text-gray-900 mb-3">
            Welcome Back
          </h1>
          <p class="text-gray-600 text-lg">
            Sign in to your account to continue
          </p>
        </div>

        <!-- Error Messages -->
        {#if error}
          <div class="bg-red-50 border border-red-200 text-red-700 px-4 py-3 rounded-xl mb-6 flex items-center" role="alert">
            <svg class="w-5 h-5 mr-3 flex-shrink-0" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M12 8v4m0 4h.01M21 12a9 9 0 11-18 0 9 9 0 0118 0z"></path>
            </svg>
            <span class="text-sm font-medium">{error}</span>
          </div>
        {/if}

        <!-- OAuth Buttons Section -->
        <div class="space-y-4 mb-8">
          <!-- Google OAuth Button -->
          <Button
            onclick={handleGoogleSignIn}
            variant="outline"
            fullWidth
            disabled={oauthLoading.google}
            class="group relative bg-white border-gray-200 text-gray-700 hover:bg-gray-50 hover:border-gray-300 font-medium text-base py-3 px-4 rounded-xl transition-all duration-200 hover:shadow-md disabled:opacity-50 disabled:cursor-not-allowed"
          >
            <div class="flex items-center justify-center space-x-3">
              {#if oauthLoading.google}
                <div class="w-5 h-5 border-2 border-blue-600 border-t-transparent rounded-full animate-spin"></div>
              {:else}
                <svg class="w-5 h-5" viewBox="0 0 24 24">
                  <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
                  <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
                  <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
                  <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
                </svg>
              {/if}
              <span>{oauthLoading.google ? 'Signing in...' : 'Continue with Google'}</span>
            </div>
          </Button>

          <!-- GitHub OAuth Button -->
          <Button
            onclick={handleGitHubSignIn}
            variant="outline"
            fullWidth
            disabled={oauthLoading.github}
            class="group relative bg-gray-900 border-gray-700 text-white hover:bg-gray-800 hover:border-gray-600 font-medium text-base py-3 px-4 rounded-xl transition-all duration-200 hover:shadow-md disabled:opacity-50 disabled:cursor-not-allowed"
          >
            <div class="flex items-center justify-center space-x-3">
              {#if oauthLoading.github}
                <div class="w-5 h-5 border-2 border-white border-t-transparent rounded-full animate-spin"></div>
              {:else}
                <svg class="w-5 h-5 fill-current" viewBox="0 0 24 24">
                  <path d="M12 0c-6.626 0-12 5.373-12 12 0 5.302 3.438 9.8 8.207 11.387.599.111.793-.261.793-.577v-2.234c-3.338.726-4.033-1.416-4.033-1.416-.546-1.387-1.333-1.756-1.333-1.756-1.089-.745.083-.729.083-.729 1.205.084 1.839 1.237 1.839 1.237 1.07 1.834 2.807 1.304 3.492.997.107-.775.418-1.305.762-1.604-2.665-.305-5.467-1.334-5.467-5.931 0-1.311.469-2.381 1.236-3.221-.124-.303-.535-1.524.117-3.176 0 0 1.008-.322 3.301 1.23.957-.266 1.983-.399 3.003-.404 1.02.005 2.047.138 3.006.404 2.291-1.552 3.297-1.23 3.297-1.23.653 1.653.242 2.874.118 3.176.77.84 1.235 1.911 1.235 3.221 0 4.609-2.807 5.624-5.479 5.921.43.372.823 1.102.823 2.222v3.293c0 .319.192.694.801.576 4.765-1.89 8.199-6.086 8.199-11.386 0-6.627-5.373-12-12-12z"/>
                </svg>
              {/if}
              <span>{oauthLoading.github ? 'Signing in...' : 'Continue with GitHub'}</span>
            </div>
          </Button>

          <!-- Divider -->
          <div class="relative">
            <div class="absolute inset-0 flex items-center">
              <div class="w-full border-t border-gray-200"></div>
            </div>
            <div class="relative flex justify-center text-sm">
              <span class="px-4 bg-white/80 text-gray-500 font-medium">or continue with email</span>
            </div>
          </div>
        </div>

        <!-- Email/Password Form -->
        <form onsubmit={handleSubmit} class="space-y-6">
          <div class="space-y-2">
            <Input
              id="email"
              name="email"
              type="email"
              label="Email Address"
              placeholder="Enter your email address"
              required
              bind:value={email}
              fullWidth
              class="bg-white border-gray-200 text-gray-900 placeholder-gray-500 focus:border-blue-500 focus:ring-blue-500 hover:border-gray-300 transition-colors duration-200"
            />
          </div>

          <div class="space-y-2">
            <Input
              id="password"
              name="password"
              type="password"
              label="Password"
              placeholder="Enter your password"
              required
              bind:value={password}
              fullWidth
              class="bg-white border-gray-200 text-gray-900 placeholder-gray-500 focus:border-blue-500 focus:ring-blue-500 hover:border-gray-300 transition-colors duration-200"
            />
          </div>

          <div class="flex items-center justify-between text-sm">
            <div class="flex items-center space-x-2">
              <div class="w-2 h-2 bg-gray-400 rounded-full"></div>
              <span class="text-gray-500">Role: <span class="font-semibold text-gray-900">{role}</span></span>
            </div>
            <a href="/forgot-password" class="text-blue-600 hover:text-blue-700 font-medium transition-colors duration-200 hover:underline">
              Forgot password?
